def get_count(sentence):
    vowels = "aeiou"
    count = 0

    for char in sentence:
        if char in vowels:
            count += 1

    return count

# Пример использования функции
sentence = "hello world"
result = get_count(sentence)
print(result)  # Выведет: 3


